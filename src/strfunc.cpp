template<typename T_Char>
std::basic_string<T_Char> join(
		const std::vector<std::basic_string<T_Char>>& array,
		const std::basic_string<T_Char>& delimit) {
	std::basic_string<T_Char> result;
	if (array.empty())
		return result;

	typename std::vector<std::basic_string<T_Char>>::const_iterator it;
	for (it = array.begin(); it != array.end(); ++it) {
		if (!result.empty())
			result += delimit;
		result += *it;
	}
	return result;
}

template
std::basic_string<char> join<char>(
		const std::vector<std::basic_string<char>>& array,
		const std::basic_string<char>& delimit);

template
std::basic_string<unsigned char> join<unsigned char>(
		const std::vector<std::basic_string<unsigned char>>& array,
		const std::basic_string<unsigned char>& delimit);

template
std::basic_string<wchar_t> join<wchar_t>(
		const std::vector<std::basic_string<wchar_t>>& array,
		const std::basic_string<wchar_t>& delimit);







